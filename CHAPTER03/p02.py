letter = '''Dear <|Name|>,
You are selected!
<|Date|>'''
print(letter.replace("<|Name|>", "Nisha").replace("<|Date|>", "20th June 2024"))

#  this is chaining of string functions, we can use multiple string functions in one line of code by chaining them together. In this example we are replacing the placeholder <|Name|> with "Nisha" and then replacing the placeholder <|Date|> with "20th June 2024" in the same line of code. This is a very powerful feature of string functions and it allows us to manipulate strings in a very efficient way.