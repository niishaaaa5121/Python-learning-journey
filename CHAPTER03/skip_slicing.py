a = "0123456789"
print(a[0:10:2])   #this means start from index 0 and go to index 10 with a step of 2
print(a[1:10:2])   #this means start from index 1 and go to index 10 with a step of 2
print(a[0:10:3])   #this means start from index 0 and go to index 10 with a step of 3
print(a[0:10:4])   #this means start from index 0 and go to index 10 with a step of 4
print(a[0:10:5])   #this means start from index 0 and go to index 10 with a step of 5             
print(a[3:9:3])    #this means start from index 3 and go to index 9 with a step of 3
print(a[3:9:4])    #this means start from index 3 and go to index 9 with a step of 4